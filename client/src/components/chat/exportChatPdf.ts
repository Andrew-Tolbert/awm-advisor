function escapeHtml(s: string): string {
  return s
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;');
}

function formatTimestamp(ms: number): string {
  return new Date(ms).toLocaleString('en-US', {
    weekday: 'short',
    month: 'short',
    day: 'numeric',
    year: 'numeric',
    hour: 'numeric',
    minute: '2-digit',
  });
}

function buildTranscript(conv: Conversation): string {
  return conv.messages
    .filter((m) => m.content.trim().length > 0)
    .map((m) => {
      if (m.role === 'user') {
        return `<div class="msg user">
  <div class="msg-label">Advisor</div>
  <div class="msg-body user-body">${escapeHtml(m.content)}</div>
</div>`;
      }
      return `<div class="msg assistant">
  <div class="msg-label">Meridian Advisor Intelligence</div>
  <div class="msg-body">${renderMarkdown(m.content)}</div>
</div>`;
    })
    .join('\n');
}

