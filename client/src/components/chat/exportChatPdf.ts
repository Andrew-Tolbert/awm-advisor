export function exportChatPdf(conv: Conversation) {
  if (typeof window === 'undefined') return;
  if (conv.messages.length === 0) {
    window.alert('Cannot export an empty conversation.');
    return;
  }
  const html = buildHtml(conv);
  const w = window.open('', '_blank');
  if (!w) {
    window.alert('Pop-up blocked. Please allow pop-ups for this site to export PDFs.');
    return;
  }
  w.document.open();
  w.document.write(html);
  w.document.close();

  const triggerPrint = () => {
    w.focus();
    w.print();
  };
  const img = w.document.querySelector('img.wordmark-img') as HTMLImageElement | null;
  if (img && !img.complete) {
    img.addEventListener('load', triggerPrint, { once: true });
    img.addEventListener('error', triggerPrint, { once: true });
    // Safety timeout in case the image never resolves.
    setTimeout(triggerPrint, 1500);
  } else {
    setTimeout(triggerPrint, 200);
  }
}
